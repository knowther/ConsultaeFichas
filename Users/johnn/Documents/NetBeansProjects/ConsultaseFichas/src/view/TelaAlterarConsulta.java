    private void preenchertable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        listacon = new ConsultaDao().getListaDay(datacatch);
        for(Consulta c : listacon){
            model.addRow(new Object[]{
                c.getNome(), c.getHora(), Utils.convertData(c.getDatasconsulta()), c.getMedicosIdmedicos().getNome()
            });
        }
    }
    
